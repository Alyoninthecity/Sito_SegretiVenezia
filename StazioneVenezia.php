<p class="testo">La Stazione della Ferrovia è un edificio largo, di concezione moderna,
    che si affaccia su un ampio piazzale aggettante sul Canal Grande.
    Ci si accede salendo una breve ma larga scalinata che porta allo spazio
    antistante alle biglietterie e ai binari. Questa è stata costruita nel 1954,
    rifacimento più ampio della prima stazione sorta nel 1860 al tempo della
    costruzione del ponte ferroviario che collegò per la prima volta la città
    lagunare alla terraferma. Questo luogo, oggi non particolarmente bello, anzi
    un po’ alienante e convulso, come lo sono tutti i dintorni trafficati delle
    stazioni importanti, rappresenta un punto topico per la storia di Venezia,
    che da qui si collega, per la prima volta della sua millenaria storia, alla
    terraferma con la costruzione del ponte ferroviario Venezia-Mestre.</p>
