<p class="testo">Il Museo di storia naturale si trova all'interno del Fontego dei Turchi, l'importante palazzo
di Venezia affacciato sul Canal Grande.<br>
Venne fondato con un decreto del Comune di Venezia del 1923 che designa il Fondaco dei
Turchi come sede del museo l'inaugurazione avvenne nel 1932.<br>
Nel 2011 il palazzo è stato consegnato completamente ristrutturato ed il percorso Museale
è stato rinnovato con 16 nuove sale, un nuovo giardino ed una nuova area ingresso.<br>
All’interno si possono vedere svariate elementi come i fossili, uno scheletro di una
balenottera e tante altre.</p>
