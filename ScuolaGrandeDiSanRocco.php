<p class="testo">La Scuola Grande di san Rocco è una Confraternita di laici fondata nel 1478. La
profonda venerazione popolare nei confronti di san Rocco, la cui reliquia era già in
possesso della Confraternita sin dal 1485, contribuì alla sua forte crescita fino a divenire
la più ricca Scuola della città.<br>
Fu allora che si decise di edificare la nuova imponente sede monumentale chiamando
poi il Tintoretto a dipingervi il suo più famoso ciclo pittorico con episodi del Vecchio e del
Nuovo Testamento. Essa è l’unica delle antiche Scuole Grandi ad essere sopravvissuta
alla caduta della Repubblica.<br>
È un luogo eccezionale dove oltre 60 dipinti sono conservati nella loro collocazione
originaria in un edificio che dalla sua costruzione non ha quasi subito modifiche.
Oggi il sodalizio è ancora attivo e persegue ancora gli antichi compiti caritativi, oltre a
curare il suo notevolissimo patrimonio artistico.
</p>
