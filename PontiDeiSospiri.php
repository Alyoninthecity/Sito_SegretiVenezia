<p class="testo">Ponte dei Sospiri È uno dei simboli della città lagunare, è considerato il ponte
dell'amore. Il Ponte dei Sospiri si ammira in tutta la sua bellezza dal vicino Ponte della
Paglia, in Riva degli Schiavoni, non lontano da piazza San Marco.<br>
Perché si chiama Ponte dei Sospiri? Secondo la tradizione popolare, a dare al ponte il
nome con cui è universalmente conosciuto fu il poeta inglese Lord Byron, che a Venezia
soggiornò per alcuni anni trovando rifugio dai debiti e dagli scandali che lo avevano
costretto a scappare dalla madrepatria. Attraverso le finestre del ponte i condannati
avrebbero visto per l'ultima volta il cielo prima di essere rinchiusi in carcere, da cui
probabilmente non sarebbero più usciti.<br>
Col tempo, però, il ponte ha assunto connotazioni e significati romantici.</p>
