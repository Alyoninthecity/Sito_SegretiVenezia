<p class="testo">il Mercato di Rialto, uno dei mercati più antichi di Venezia, è situato nei pressi
dell'omonimo Ponte di Rialto, nel cuore del centro storico di Venezia, e si estende tra
Campo de la Pescaria ed il bel Campo San Giacometo.<br>
Il mercato di Rialto ospita il tradizionale mecato del pesce e il mercatino di frutta e
verdura.<br>
È vero punto fermo dell'enogastronomia locale, indispensabile per preparare piatti tipici
veneziani. Frutte e verdura di qualità e pesce fresco delle acque lagunari e limitrofe sono
una vera garanzia al Mercato di Rialto.<br>
Orari utili:<br>
Mercato del pesce: dal martedì al sabato, dalle ore 7,30 alle 12,00.<br>
Mercato di frutta e verdura: dal lunedì al sabato, dalle ore 7,30 e 13,30.</p>
