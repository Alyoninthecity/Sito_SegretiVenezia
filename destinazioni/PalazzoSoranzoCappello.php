<p class="testo">Palazzo Soranzo Cappello è ubicato nel sestiere di Santa Croce (770), vicino a Palazzo
Gradenigo. È sede della Soprintendenza per i beni architettonici e per il paesaggio per
le province di Venezia, Belluno, Padova e Treviso.<br>
Il palazzo fu eretto nel tardo XVI secolo.<br>
Internamente il palazzo presenta pregevoli opere pittoriche e decorative.<br>
Il complesso costituisce, con l’attiguo palazzetto, la corte e il vasto giardino, un notevole
compendio monumentale separato dal muro di cinta del giardino di Palazzo Gradenigo. </p>
