<p class="testo">Usciti dalla stazione di Venezia Santa Lucia e proseguendo per Strada Nuova si svolta a
sinistra dopo il ponte delle Guglie. Sulla destra si apre un sottoportego. E’ l’ingresso
del Ghetto ebraico di Venezia, l’antica zona della città dove hanno vissuto gli ebrei a
partire dal 1300 fino alla Seconda Guerra Mondiale.<br>
In quest’area inizialmente sorgevano le fonderie di Venezia (ghetto deriva dalla parola
veneziana “géto” che significa fondere
successivamente quest’isola venne concessa agli ebrei nel 1300 espropriandola alle
famiglie che vi abitavano e permettendo così a questa minoranza di entrare a far parte
della vita sociale ed economica di Venezia.<br>
Si riconosce subito di essere nel ghetto per la particolare costruzione degli edificie: sono
alti fino a 8 piani e stretti.<br>
E’ incredibile pensare come, nei momenti di massima espansione, in questa città nella
città vivessero fino a 5.000 persone. In pratica ognuno aveva a disposizione 2/3 metri
quadrati a testa!</p>
